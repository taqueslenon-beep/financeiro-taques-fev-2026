/**
 * Lançamentos reais do sistema financeiro.
 *
 * settlementDate: data de efetivação (pagamento ou recebimento).
 * Quando vazio/null, o lançamento é apenas projetado.
 */

function generateInstallments({ baseId, description, amount, startDate, count, accountId, categoryId }) {
  const result = []
  for (let i = 0; i < count; i++) {
    const d = new Date(startDate + 'T12:00:00')
    d.setMonth(d.getMonth() + i)
    const yyyy = d.getFullYear()
    const mm = String(d.getMonth() + 1).padStart(2, '0')
    const dd = String(d.getDate()).padStart(2, '0')
    result.push({
      id: baseId + i,
      description: `${description} (Parcela ${i + 1}/${count})`,
      amount: -Math.abs(amount),
      dueDate: `${yyyy}-${mm}-${dd}`,
      settlementDate: '',
      type: 'Despesa',
      status: 'pendente',
      recurrence: 'Parcelamento',
      accountId,
      categoryId,
    })
  }
  return result
}

